// This is a flag for which song is being played. 
#define TITLE_SONG 0
#define CHARSELECT_SONG 1
#define GHZ_SONG 2
#define SBZ_SONG 3
#define RESULT_SONG 4
uint8_t PianoMode = 5; 

int main(void){     
  char c1, c2, c3, c4, c5, c6, c7, c8; 
  // PLL, Texas stuff. 
  TExaS_Init(SW_PIN_PE3210,DAC_PIN_PB3210,ScopeOn);
  // Piano wrapper stuff.   
  Piano_Init();
  // DAC stuff. 
  DAC_Init_6bits();
  UART1_Init(); 
  
  // 80: 1 microsecond. This init allows updates every 5 microseconds. 
  Sound_Init(400);   // was 400
  // CSPIE.   
  EnableInterrupts();
  // Song playback stuff. 
  EnableSongPlayback(); 
  
  while(1){ 
    // Heartbeat
    Piano_In(); 
    // Handle FIFO stuff. 
    while (FiFo_Get(&c1) == 0 || c1 != 0x12) ; 
    while (FiFo_Get(&c2) == 0 || c2 != 0x34) ; 
    while (FiFo_Get(&c3) == 0) ; // Get the data
    while (FiFo_Get(&c4) == 0) ; // Get the data
    while (FiFo_Get(&c5) == 0) ; // Get the data
    while (FiFo_Get(&c6) == 0) ; // Get the data
    while (FiFo_Get(&c7) == 0 || c7 != 0x56) ; 
    while (FiFo_Get(&c8) == 0 || c8 != 0x78) ; 
    if (c3 == c4 && c4 == c5 && c5 == c6) {
      SwitchSong(c3); 
    }
  }  
}

void EnableSongPlayback(void) {
  Timer0A_Init(&NextBeat, 7000000); 
}

void DisableSongPlayback(void) {
  PianoMode = 0;  
  songPosition = 0;
}

void NextBeat(void) {
  uint8_t count; 
  
  switch (PianoMode) {
    case 0:
      if (songPosition >= 83) break;
      for (count = 0; count < TitleSong[songPosition].NoteNumber; count++) {
        noteOn(TitleSong[songPosition].Notes[count] - 18); 
      }
      songPosition++; 
      if (songPosition >= 84) songPosition = 0;
      break; 
    case 1:
      for (count = 0; count < OptionsSong[songPosition].NoteNumber; count++) {
        noteOn(OptionsSong[songPosition].Notes[count] - 18); 
      }
      songPosition++; 
      if (songPosition >= 599) songPosition = 0;
      break; 
    case 2:
      for (count = 0; count < GHZSong[songPosition].NoteNumber; count++) {
        noteOn(GHZSong[songPosition].Notes[count] - 18); 
      }
      songPosition++; 
      if (songPosition >= 785) songPosition = 0;
      break; 
    case 3:
      for (count = 0; count < SBZSong[songPosition].NoteNumber; count++) {
        noteOn(SBZSong[songPosition].Notes[count] - 18); 
      }
      songPosition++; 
      if (songPosition >= 1194) songPosition = 0;
      break; 
    case 4:
      if (songPosition >= 32) break;
      for (count = 0; count < ResultsSong[songPosition].NoteNumber; count++) {
        noteOn(ResultsSong[songPosition].Notes[count] - 18); 
      }
      songPosition++; 
      break;
    case 5:
      break; 
  }
} 

void SwitchSong(uint8_t new_Song) {
  if (new_Song == PianoMode) return; 
  PianoMode = new_Song;
  songPosition = 0; 
}

